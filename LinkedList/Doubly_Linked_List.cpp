class Solution {
  public:
    Node* constructDLL(vector<int>& arr) {
        int n=arr.size();
        Node* head=NULL;
        Node* node= new Node(arr[0]);
        head=node;
        Node* current=head;
        Node* previous=NULL;
        for(int i=1;i<n;i++){
            Node* newNode= new Node(arr[i]);
            previous=current;
            current->next=newNode;
            newNode->prev=current;
            current=newNode;
        }
        return head;
    }
};

